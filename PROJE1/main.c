// UART'tan veri geldiğinde burası çalışır (USART2 Alıcı)
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if(huart->Instance == USART1)
    {
        // Gelen veriye göre DAC çıkışını ayarla
        // DAC 12-bit (0 - 4095). 3.3V referans.
        // 1 Volt ~= 1240
        // 2 Volt ~= 2481
        // 3 Volt ~= 3722

        uint32_t dac_val = 0;

        if(rx_data >= 0 && rx_data <= 100)
        {
            dac_val = 1240; // ~1 Volt
        }
        else if(rx_data > 100 && rx_data <= 200)
        {
            dac_val = 2481; // ~2 Volt
        }
        else if(rx_data > 200)
        {
            dac_val = 3722; // ~3 Volt
        }

        // Değeri DAC'a yaz
        HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_R, dac_val);

        // Bir sonraki veriyi beklemek için alıcıyı tekrar kur
        HAL_UART_Receive_IT(&huart1, (uint8_t*)&rx_data, 1);
    }
}

